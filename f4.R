v <- TRUE 
print(class(v))
#Numeric  12.3, 5, 999  
v <- 23.5
print(class(v))
#Integer  2L, 34L, 0L  
v <- 2L
print(class(v))
#Complex  3 + 2i  
v <- 2+5i
print(class(v))
#Character  'a' , '"good", "TRUE", '23.4'  
v <- "TRUE"
print(class(v))
#Raw  "Hello" is stored as 48 65 6c 6c 6f  
v <- charToRaw("Hello")
print(class(v))
